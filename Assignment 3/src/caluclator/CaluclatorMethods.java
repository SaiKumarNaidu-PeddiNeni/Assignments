package caluclator;

public class CaluclatorMethods {
	float number1;
	float number2;

	float Addition(float number1, float number2) {
		float sum;
		sum = number1 + number2;
		return sum;
	}

	float Substraction(float number1, float number2) {
		float sub;
		sub = number1 - number2;
		return sub;
	}

	float Multiplication(float number1, float number2) {
		float mul;
		mul = number1 * number2;
		return mul;
	}

	float Division(float number1, float number2) {
		float div;
		div = number1 / number2;
		return div;
	}

}
